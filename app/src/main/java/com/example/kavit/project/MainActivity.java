    private void populateListView(DatabaseHandler db) {
        Cursor c=db.getAllRows();
        String[] fromFieldNames=new String[]{DatabaseHandler._ID, DatabaseHandler.MOVIE_NAME};
        int[] toViewIDs=new int[] {R.id.textView1,R.id.textView2};
        SimpleCursorAdapter myCursorAdapter;
        myCursorAdapter=new SimpleCursorAdapter(getBaseContext(),R.layout.item_layout,c,fromFieldNames,toViewIDs,0);
        myListView.setAdapter(myCursorAdapter);
    }

    @Override
    public void onBackPressed() {
        DrawerLayout drawer = (DrawerLayout) findViewById(R.id.drawer_layout);
        if (drawer.isDrawerOpen(GravityCompat.START)) {
            drawer.closeDrawer(GravityCompat.START);
        } else {
            super.onBackPressed();
        }
    }

